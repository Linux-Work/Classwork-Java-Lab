class operator{
	public static void main(String[] args){
		int a = 5;
		int b = 3;
		System.out.println("Value of a: " + a + "\nValue of b: " + b);
		System.out.println("a++ Operator: " + (a++));
		System.out.println("a-- Operator: " + (a--));
		System.out.println("++a Operator: " + (++a));
		System.out.println("--a Operator: " + (--a));
		System.out.println("a+=b Operator: " + (a+=b));
		System.out.println("a-=b Operator: " + (a-=b));
		System.out.println("a*=b Operator: " + (a*=b));
		System.out.println("a/=b Operator: " + (a/=b));
	}
}